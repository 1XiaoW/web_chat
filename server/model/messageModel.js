const mongoose = require('mongoose');

// messages表模型
const messageSchema = new mongoose.Schema(
  {
    message: {
      text: { type: String, required: true },
    },
    users: Array,
    sender: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

// 导出模型
module.exports = mongoose.model('Messages', messageSchema);
